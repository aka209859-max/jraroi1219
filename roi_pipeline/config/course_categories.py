# 競馬場コード → 名称の変換テーブル
KEIBAJO_CODE_TO_NAME: Dict[str, str] = {
    "01": "札幌", "02": "函館", "03": "福島", "04": "新潟", "05": "東京",
    "06": "中山", "07": "中京", "08": "京都", "09": "阪神", "10": "小倉",
}


def get_category(keibajo_code: str, surface: str, distance: int) -> str:
    """
    競馬場コード・馬場種別・距離から27カテゴリ名を返す。

    Args:
        keibajo_code: 競馬場コード（"01"〜"10"）
        surface: 馬場種別（"芝" or "ダ"）
        distance: 距離（int、例: 1200）

    Returns:
        カテゴリ名。該当なしの場合は "unknown"。

    Note:
        距離文字列には"内"/"外"が付くケースがあるため、
        まず完全一致を試み、次に数値のみで検索する。
    """
    distance_str = str(distance)
    for cat_name, entries in ALL_CATEGORIES.items():
        for code, surf, dist_str in entries:
            if code == keibajo_code and surf == surface:
                # 完全一致
                if dist_str == distance_str:
                    return cat_name
                # 数値部分のみ一致（"1400内" → "1400"）
                dist_num = "".join(c for c in dist_str if c.isdigit())
                if dist_num == distance_str:
                    return cat_name
    return "unknown"
